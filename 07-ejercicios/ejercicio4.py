"""
Ejercicio No.4. Pedir dos numeros al usuario y hacer todas las 
operaciones básicas de una calculadora y mostrarlo en pantalla.
"""

numero1 = int(input("Introduce el primer número: "))
numero2 = int(input("Introduce el segundo número: "))

print("##### CALCULADOR #####")
print("suma: " + str(numero1+numero2))
print("Resta: " + str(numero1-numero2))
print("Multiplicación: " + str(numero1*numero2))
print("División: " + str(numero1/numero2))
